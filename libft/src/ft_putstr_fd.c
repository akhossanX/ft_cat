#include "libft.h"

int			ft_putstr_fd(int fd, const char *s)
{
	int		i;

	if (fd < 0)
		return (0);
	i = 0;
	while (*s)
	{
		i += ft_putchar_fd(fd, *s);
		s++;
	}
	return (i);
}
